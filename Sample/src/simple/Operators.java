package simple;

public class Operators {
public static void main(String[]args) {
	int a=33;
	int b=45;
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(a/b);
	System.out.println(a%b);
	
	int n =67;
	n+=99;
	System.out.println(n);
	
	int x=33;
	int y=76;
	System.out.println(x>y);
}
}
